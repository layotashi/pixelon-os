/**
 * roll_grid_lines.test.js — ROLL の時間方向 (縦) グリッド線の階層と点線位相を検証する。
 *
 * 仕様:
 *   小節線 = 2px 実線 / 拍線 = 1px 実線 / ステップ (拍より細かい) = 1px 点線。
 *   点線は各セル内寸の上端から 1px おきに点を打つ。行高 (cellH) は常に奇数なので内寸も
 *   奇数となり、点は内寸の上端と下端の両方に乗る。よって隙間が横罫線の行にちょうど重なり、
 *   横線を上下 1px の点で挟んだ左右対称な 3px の交点になる。縦ズームは 2px 刻みで奇数を保つ。
 *
 * 点線位相を司る drawStepDots を gpu プリミティブ経由で実バッファ (vram) へ描き、pget で
 * 1px ずつ読んでユーザー提供の ASCII 仕様と完全一致 (diff 0) することを確かめる。
 */
import { describe, it, expect, beforeEach } from "vitest";
import { drawStepDots } from "@/app/roll/roll.js";
import { fillRect, pget } from "@/core/gpu.js";

/** (x0..x1, y0..y1) の矩形を "#"(=1)/"."(=0) の行文字列配列にする */
function readAscii(x0, y0, x1, y1) {
  const lines = [];
  for (let y = y0; y <= y1; y++) {
    let s = "";
    for (let x = x0; x <= x1; x++) s += pget(x, y) ? "#" : ".";
    lines.push(s);
  }
  return lines;
}

describe("ROLL 縦グリッド — ステップ点線の位相 (drawStepDots)", () => {
  beforeEach(() => fillRect(0, 0, 32, 24, 0)); // 検証域を背景 0 でクリア

  it("ユーザー ASCII 仕様と完全一致する (横線を上下 1px の点で対称に挟む)", () => {
    // ユーザー提供仕様の完全再現: 幅 3 × 高さ 13。中央 (x=1) に縦点線 1 本、
    // 横実線 2 本 (y=3, 9)。横線に挟まれた内寸は 5px (y=4..8) の奇数。点線は各内寸の
    // 上端・下端の両方に乗り、その隙間が横線の行にちょうど重なる → 交点は「点・線・点」の
    // 3px で上下対称になる。
    const SPEC = [
      ".#.", // y0  内寸上端 (点)
      "...", // y1  隙間
      ".#.", // y2  内寸下端 (点) — 直下の横線を上から挟む
      "###", // y3  横線 (隙間に重なる)
      ".#.", // y4  内寸上端 (点) — 横線を下から挟む
      "...", // y5
      ".#.", // y6
      "...", // y7
      ".#.", // y8  内寸下端 (点)
      "###", // y9  横線
      ".#.", // y10
      "...", // y11
      ".#.", // y12
    ];
    // 3 帯の内寸上端 Y (いずれも ch=5)。上帯は最後の点が y=2、中帯は y=4..8、下帯は y=10..。
    const interiorY = [-2, 4, 10];
    drawStepDots(1, 0, interiorY, 3, 5);
    // 横罫線 (全幅) を後から重ねる。点線は内寸のみに乗るため交点で衝突しない。
    fillRect(0, 3, 3, 1, 1);
    fillRect(0, 9, 3, 1, 1);

    expect(readAscii(0, 0, 2, 12)).toEqual(SPEC);
  });

  it("複数の点線列・別の行高 (ズーム後) でも同じ位相で揃う", () => {
    // 幅 9 × 高さ 11。縦点線 2 本 (x=1,7)、横実線 2 本 (y=1,9)。行高 ch=7 (奇数)。
    // 内寸は 7px (y=2..8)。上端 (y=2) と下端 (y=8) の両方が点になり、隙間 (奇数行) が
    // 横線 (y=1,9) に一致する。2 本とも位相が完全に揃う。
    const SPEC = [
      ".#.....#.",
      "#########",
      ".#.....#.",
      ".........",
      ".#.....#.",
      ".........",
      ".#.....#.",
      ".........",
      ".#.....#.",
      "#########",
      ".#.....#.",
    ];
    const interiorY = [-6, 2, 10]; // 3 帯の内寸上端。いずれも ch=7。
    drawStepDots(1, 0, interiorY, 3, 7);
    drawStepDots(7, 0, interiorY, 3, 7);
    fillRect(0, 1, 9, 1, 1);
    fillRect(0, 9, 9, 1, 1);

    expect(readAscii(0, 0, 8, 10)).toEqual(SPEC);
  });

  it("行高が奇数なら内寸の上端と下端の両方に点が乗る (横線を上下対称に挟む)", () => {
    // 内寸 y=2..8 (ch=7, 奇数)。上の横線 y=1・下の横線 y=9 の直下/直上が両方とも点になり、
    // 横線を 3px (点・線・点) で対称に挟む。行高は常に奇数なので必ずこの位相になる。
    drawStepDots(5, 0, [2], 1, 7);
    expect(pget(5, 2)).toBe(1); // 内寸上端 = 点 (上の横線 y=1 の直下)
    expect(pget(5, 8)).toBe(1); // 内寸下端 = 点 (下の横線 y=9 の直上)
    expect(pget(5, 1)).toBe(0); // 上の横線行は隙間 (点は乗らない)
    expect(pget(5, 9)).toBe(0); // 下の横線行も隙間
    expect([2, 4, 6, 8].every((y) => pget(5, y) === 1)).toBe(true); // 内寸は 1px おきに点
    expect([3, 5, 7].every((y) => pget(5, y) === 0)).toBe(true); // その隙間
  });
});
